SELECT 
     CASE
          WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 7 THEN  1
          WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 30 THEN  8
          WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 60 THEN  31
          WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 90 THEN  61
          ELSE 91
       END FAIXAINICIO,
     	CASE
          WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 7 THEN  8
          WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 30 THEN  30
          WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 60 THEN  60
          WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 90 THEN  90
          ELSE NULL
          END FAIXAFIM,
     CASE   
            WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 7 THEN  'ATÉ 7 DIAS'
            WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 30 THEN  'ENTRE 8 e 30 DIAS'
            WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 60 THEN  'ENTRE 31 e 60 DIAS'
            WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 90 THEN  'ENTRE 61 e 90 DIAS'
          ELSE ' MAIS DE 90 DIAS'
       END TEXTOFAIXA,  
   SUM(FIN.VLRBAIXA) VALOR ,
   COUNT(FIN.NUFIN)  QTD 
  FROM TGFFIN FIN
  INNER JOIN TGFPAR PAR
  ON FIN.CODPARC = PAR.CODPARC
  WHERE
  FIN.VLRBAIXA <> 0
  AND TRUNC(FIN.DHBAIXA) BETWEEN  TRUNC(SYSDATE) - 7  AND  TRUNC(SYSDATE)
  AND PAR.CODGRUPO = 2
  AND FIN.CODREGUA = 2 
   GROUP BY      
     CASE
          WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 7 THEN  1
          WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 30 THEN  8
          WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 60 THEN  31
          WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 90 THEN  61
          ELSE 91
       END ,
     	CASE
          WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 7 THEN  8
          WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 30 THEN  30
          WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 60 THEN  60
          WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 90 THEN  90
          ELSE NULL
       END ,
CASE    
            WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 7 THEN  'ATÉ 7 DIAS'
            WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 30 THEN  'ENTRE 8 e 30 DIAS'
            WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 60 THEN  'ENTRE 31 e 60 DIAS'
            WHEN TRUNC (SYSDATE) - FIN.DTVENC <= 90 THEN  'ENTRE 61 e 90 DIAS'
          ELSE ' MAIS DE 90 DIAS'
       END
  ORDER BY 1
